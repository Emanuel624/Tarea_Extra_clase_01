/**
 * Clase Resta, utilizada para realizar la operación de Resta entre los valores de
 * edades asociados a las personas generadas por medio de la interfaz.
 */

public class Resta {
    /**
     * @param num1 primer parámetro int necesario para poder realizar la operación de Resta
     * @param num2 Segundo parámetro int necesario para poder realizar la operación de Resta
     * @return se retorna el valor obtenido de Restr el primer parametro con el segundo
     */
    public int RestaOperacion(int num1, int num2){
        return num1 - num2;
    }
      
}

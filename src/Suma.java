/**
 * Clase suma, utilizada para realizar la operación de suma entre los valores de
 * edades asociados a las personas generadas por medio de la interfaz.
 */
public class Suma {
    /**
     * @param num1 primer parámetro int necesario para poder realizar la operación de suma
     * @param num2 segundo parámetro int necesario para poder realizar la operación de suma
     * @return se retorna el valor obtenido de sumar el primer parametro con el segundo
     */
    
    public int sumarOperacion(int num1, int num2){
        return num1 + num2;
    }
}          

/**
 * Clase Multiplicacion, utilizada para realizar la operación de Multiplicacion entre los valores de
 * edades asociados a las personas generadas por medio de la interfaz.
 */
public class Multiplicacion {
    /** 
     * @param num1 primer parámetro int necesario para poder realizar la operación de Multiplicacion
     * @param num2 segundo parámetro int necesario para poder realizar la operación de Multiplicacion
     * @return se retorna el valor obtenido de multiplicar el primer parametro con el segundo
     */
    public int MultOperacion(int num1, int num2){
        return num1 * num2;
    }
          
}
